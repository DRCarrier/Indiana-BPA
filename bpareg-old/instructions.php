<p><font face="Times New Roman, Times, serif"><em><strong>Displaying/Editing Student and 
  Advisor Lists:</strong></em></font></p>
<p><font face="Times New Roman, Times, serif">Click the &quot;View/Edit Student 
  List&quot; or &quot;View/Edit Advisor List&quot; link to display your list of 
  students/advisors. Verify that all information is correct. If you find errors, click the student's ID 
  number or advisor's ID number to make corrections. To delete a student or advisor click the &quot;Delete&quot; link.</font></p>
<p><em><strong><font face="Times New Roman, Times, serif">Housing Information:</font></strong></em></p>
<p><font face="Times New Roman, Times, serif">Before entering your housing information, be sure to print both the advisor and student lists. You will need the ID numbers for advisors/chaperones and students to enter the housing information.  <strong><em>DO NOT USE THE ADVISOR ID ASSIGNED BY THE NATIONAL BPA MEMBERSHIP SYSTEM. USE THE ID LISTED ON THE ADVISOR LIST PRINTED FROM THE SLC REGISTRATION SITE.</strong></em></font></p>
<ol>
  <li><font face="Times New Roman, Times, serif">Click the &quot;Enter Housing Information&quot; link.</font></li>
  <li><font face="Times New Roman, Times, serif"><em>Advisor or Student Room:</em> Select the type of room. Select A if an advisor or advisor(s) will be staying in the room. Select an S if only students will be in the room.</font></li>
  <li><em>Gender: </em>Select M if males will be staying in the room. Select F if females will be staying in the room. The M/F option is for an advisor room only.</li>
  <li><em>Room Type:</em> . Select the room type for the total number of people who will be staying in the room, even if some of the students/advisors are from another school. Example: 2 students from your school and 2 students from another school is a Quad (Q) room.)</li>
  <li>Enter the ID numbers for the occupants of the room. You can find these ID numbers on your Student List and Advisor List that you printed earlier. <em><strong>For advisors, DO NOT use their national membership number; use the ID number on your Advisor List.</strong></em><br>
    <ul>
      <li>  <em>Room Sharing: </em>If advisors and/or students from your school will be sharing room(s) with another school(s), you and the other advisor(s) will need to decide who is going to enter the occupants for the shared room(s). The advisor who decides to enter the information for the shared room will need to get the advisor and/or student ID numbers from the other advisor(s). <em><strong>When the invoice is generated each school will be billed for its own students.</strong></em></li>
    </ul>
  </li>

  <li>If you need to enter additional rooms, click the &quot;Add Another Room&quot; button and follow step #2-5. </li>
  <li>When you have entered all your rooms, click the &quot;Finished Adding Rooms&quot; button.</li>
  <li>You may verify your housing information by clicking on the &quot;View/Edit Housing Information&quot; link on the SLC Registration home page.
    <ul>
      <li>Click the &quot;Room ID Number&quot; link to edit the room information</li>
      <li>Click the &quot;Delete&quot; link to delete the room</li>
    </ul>
  </li>
</ol>
