    Open Contests Only&quot; if this is the only activity the student is participating in (i..e., not a voting delegate, officer, officer candidate, etc.)</font></li>
      <li><strong>CALCULATING VOTING DELEGATES:</strong> Each chapter receives one voting 
    delegate plus one for each 10 members or greater portion thereof. (i.e., A 
    chapter with 23 members gets 3 voting delegates. A chapter with 25 members 
