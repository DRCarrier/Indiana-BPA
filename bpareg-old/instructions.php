<p><font face="Times New Roman, Times, serif"><em><strong>Student Information:</strong></em></font></p>
<ol>
  <li><font face="Times New Roman, Times, serif">The registration database already includes all students who were  registered online for RLC.  Students who were registered for RLC after the online registration closed may not be in the database, and you will need to add them.</font></li>
  <li><font face="Times New Roman, Times, serif">Click the &quot;View/Edit Student List&quot; link to display all the students who are in the database for your school.</font></li>
  <li><font face="Times New Roman, Times, serif">Press the Delete link for any students who will not be attending SLC.</font> Your chapter will be charged $55 for each student who remains in the database, so be sure to delete all students who are not attending SLC.</li>
  <li><font face="Times New Roman, Times, serif">Click the ID number link to edit a student's information.</font>
    <ul>
      <li><font face="Times New Roman, Times, serif">Make sure students are entered in the correct contest(s). If a student did not qualify for SLC in an event, you need to delete the event for that student.  If you made a change in a student's contest after online RLC registration closed, you will need to update the event number as well.</font></li>
      <li>You must select a shirt size for each student. If you do not select a size, a shirt will not be ordered for the student.
        <ul>
          <li>s = small</li>
          <li>m = medium</li>
          <li>l = large</li>
          <li>xl = xlarge</li>
          <li>xxl = xxlarge</li>
          <li>xxxl = xxxlarge</li>
        </ul>
      </li>
      <li><font face="Times New Roman, Times, serif">Check any of the items that apply for the student (i.e., current state officer, officer candidate, voting delegate, etc.)</font></li>
      <li><font face="Times New Roman, Times, serif">Check the &quot;Open Events Only&quot; box if this is the only activity the student is participating in (i..e., not a voting delegate, officer, officer candidate, etc.)</font></li>
      <li>Click the &quot;Save Changes&quot; button when you have made all the necessary changes for the student.</li>
    </ul>
  </li>

  <li><font face="Times New Roman, Times, serif">If you need to add a student(s), click the &quot;Add Students&quot; link on the SLC Registration home page</font>
    <ul>
      <li><font face="Times New Roman, Times, serif">Enter the student's NLC membership ID # (use hyphens: 24-xxxx-xxxx)</font>.</li>
      <li><font face="Times New Roman, Times, serif">Enter the first name, last name, and contests the student will be competing in.</font></li>
      <li><font face="Times New Roman, Times, serif">Place a check mark beside any 
